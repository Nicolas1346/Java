package com.mycompany.quinto;

public class Quinto {

    public static void main(String[] args) {
       String n = "Primeiro";
       
       char letra = n.charAt(0);
       char letra2 = n.charAt(n.length() - 1);
       
       System.out.println("Primira: " + letra);
       System.out.println("Segunda: " + letra2);
    }
}
